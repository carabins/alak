describe('CRDT + atomic smoke test', () => {
  test('@crdt(LWW_MAP) emits merge/lww_key; @atomic emits encode_cbor/decode_cbor', () => {
    const src = `
      schema Test {
        version: 1
        namespace: "crdt_test"
      }
      record Message @crdt(type: LWW_MAP, key: "updated_at") {
        id: ID!
        text: String!
        updated_at: Timestamp!
      }
      record KalmanBlock @atomic {
        matrix: [Float!]!
        bias: [Float!]!
      }
    `
    const res = compileSources([{ path: 't.aql', source: src }])
    expect(res.diagnostics.filter(d => d.severity === 'error')).toEqual([])
    const gen = generate(res.ir!, { namespace: 'crdt_test' })
    const out = gen.files[0].content
    expect(out).toContain(`pub fn crdt_type() -> &'static str { "LWW_MAP" }`)
    expect(out).toContain(`pub fn lww_key(&self) -> i64 {`)
    expect(out).toContain(`self.updated_at`)
    expect(out).toContain(`pub fn merge(a: &Self, b: &Self) -> Self {`)
    expect(out).toContain(`pub fn encode_cbor(&self) -> Result<Vec<u8>, serde_cbor::Error> {`)
    expect(out).toContain(`pub fn decode_cbor(bytes: &[u8]) -> Result<Self, serde_cbor::Error> {`)
    // Cargo header advertises cbor + automerge deps because of the directives.
    expect(out).toContain(`serde_cbor = "0.11"`)
    expect(out).toContain(`automerge = "0.6"`)
  })

  test('unsupported CRDT type emits stub + warning', () => {
    const src = `
      schema Test {
        version: 1
        namespace: "or_set_test"
      }
      record Tags @crdt(type: OR_SET) {
        id: ID!
        values: [String!]!
      }
    `
    const res = compileSources([{ path: 't.aql', source: src }])
    const gen = generate(res.ir!, { namespace: 'or_set_test' })
    const out = gen.files[0].content
    expect(out).toContain(`// TODO: @crdt(type: OR_SET)`)
    const hasWarning = gen.diagnostics.some(d =>
      d.severity === 'warning' && d.message.includes('OR_SET'),
    )
    expect(hasWarning).toBe(true)
  })
})

