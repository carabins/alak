export { GENERATOR_NAME, GENERATOR_VERSION }

// Re-export graph types so downstream generators-of-generators can type
// their IR arguments from a single import site (same convention as the
// sibling graph-link-state package).
export type { IR, IRSchema, IRRecord, IRAction } from '@alaq/graph'
